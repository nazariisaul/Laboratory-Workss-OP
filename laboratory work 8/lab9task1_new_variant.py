# Функція для перевірки коректності введення
def is_correct_array(s):
    i = 0
    while i < len(s):
        # Дозволяємо цифри, пробіли та знак мінуса для від'ємних чисел
        if not (s[i].isdigit() or s[i] == ' ' or s[i] == '-'):
            return False
        i = i + 1
    return True

# Введення даних
s = input("Введіть елементи масиву через пробіл: ")

# 1. Перевірка на наявність літер або спецсимволів
if not is_correct_array(s):
    print("Неможливо розв’язати: введено заборонені символи (літери тощо)!")
else:
    # Очищення від зайвих пробілів по боках та перетворення у список
    s = s.strip()
    if s == "":
        mas = []
    else:
        # map(int, ...) перетворює кожен елемент рядка на ціле число
        mas = list(map(int, s.split()))

    # 2. Перевірка, чи не порожній масив
    if len(mas) == 0:
        print("Масив порожній, обчислення неможливе.")
    else:
        # Ініціалізація змінних для розрахунків
        s_sum = 0  # Сума
        p = 1  # Добуток
        count = 0  # Кількість

        i = 0
        while i < len(mas):
            val = mas[i]


            # Частина яка віднізняється_____
            # ЛОГІКА БЕЗ ЗНАЧКА % (ОСТАЧІ)
            # Число ділиться на 3 і на 5 одночасно, якщо воно ділиться на 15.
            # Перевірка: якщо при діленні націло (//) на 15 і подальшому
            # множенні на 15 ми отримуємо те саме число — воно кратне 15.
            if (val // 15) * 15 == val:
                s_sum = s_sum + val
                p = p * val
                count = count + 1
            i = i + 1
            # Частина яка віднізняється_____


        # 3. Виведення результатів
        if count == 0:
            print("Елементів, що діляться на 3 і на 5 (тобто на 15), не знайдено.")
        else:
            print(f"Знайдено елементів:", count)
            print(f"Сума цих елементів: ", s_sum)
            print(f"Добуток цих елементів: ", p)