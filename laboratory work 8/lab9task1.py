# Перевірка: рядок містить лише цифри, пробіли та мінус
def is_correct_array(s):
    i = 0
    while i < len(s):
        if not (s[i].isdigit() or s[i] == ' ' or s[i] == '-'):
            return False
        i = i + 1
    return True

# Введення масиву
s = input("Введіть елементи масиву через пробіл: ")

# Перевірка: чи не містить введений рядок літер
if not is_correct_array(s):
    print("Неможливо розв’язати: введено літери!")
else:
    # Якщо введено порожній рядок — масив порожній
    if s.strip() == "":
        mas = []
    else:
        # Перетворення рядка у список цілих чисел
        mas = list(map(int, s.split()))

    # Перевірка: чи масив не порожній
    if len(mas) == 0:
        print("Масив порожній, обчислення неможливе.")
    else:
        # Ініціалізація змінних
        s_sum = 0     # сума потрібних елементів
        p = 1         # добуток потрібних елементів
        count = 0     # кількість потрібних елементів

        # Перегляд масиву за допомогою циклу while
        i = 0
        while i < len(mas):
            # Перевірка умови подільності на 3 і на 5
            if mas[i] % 3 == 0 and mas[i] % 5 == 0:
                s_sum = s_sum + mas[i]
                p = p * mas[i]
                count = count + 1
            i = i + 1

        # Виведення результатів
        if count == 0:
            print("Елементів, що діляться на 3 і на 5, не знайдено.")
        else:
            print("Кількість:", count)
            print("Сума:", s_sum)
            print("Добуток:", p)